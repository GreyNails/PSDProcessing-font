def download_font(font_name, save_dir="fonts"):
    """
    直接下载字体ZIP文件
    """
    # 创建保存目录
    if not os.path.exists(save_dir):
        os.makedirs(save_dir)
    
    # 构建下载URL
    # url = f"https://www.freefontdownload.org/download-font/{font_name}"
    url = f"https://www.freefontdownload.org/download-font-otf/{font_name}"

    
    # 设置请求头
    headers = {
        'User-Agent': 'Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36 (KHTML, like Gecko) Chrome/91.0.4472.124 Safari/537.36'
    }
    
    try:
        print(f"正在下载: {font_name}")
        print(f"  URL: {url}")
        
        # 直接下载文件
        response = requests.get(url, headers=headers, timeout=30)
        response.raise_for_status()
        
        # 检查是否是ZIP文件
        content_type = response.headers.get('content-type', '')
        if 'zip' in content_type or 'application' in content_type:
            # 保存为ZIP文件
            file_path = os.path.join(save_dir, f"{font_name}.zip")
            with open(file_path, 'wb') as f:
                f.write(response.content)
            
            file_size = len(response.content) / 1024  # KB
            print(f"  ✅ 成功下载: {file_path} ({file_size:.2f} KB)")
            return True
        else:
            print(f"  ❌ 响应不是ZIP文件，Content-Type: {content_type}")
            return False
        
    except requests.exceptions.HTTPError as e:
        if e.response.status_code == 404:
            print(f"  ❌ 字体不存在 (404)")
        else:
            print(f"  ❌ HTTP错误: {e}")
        return False
    except requests.exceptions.RequestException as e:
        print(f"  ❌ 下载出错: {e}")
        return False
    except Exception as e:
        print(f"  ❌ 未知错误: {e}")
        return False

def main():
    """
    主函数：批量下载所有字体
    """
    print("=" * 60)
    print("开始批量下载字体")
    print("=" * 60)
    print()
    
    success_count = 0
    fail_count = 0
    failed_fonts = []
    
    for i, font_name in enumerate(fonts, 1):
        print(f"[{i}/{len(fonts)}] {font_name}")
        
        if download_font(font_name):
            success_count += 1
        else:
            fail_count += 1
            failed_fonts.append(font_name)
        
        # 添加延迟，避免请求过快
        if i < len(fonts):
            time.sleep(1.5)  # 每次请求间隔1.5秒
        
        print()
    
    print("=" * 60)
    print("下载完成！")
    print("=" * 60)
    print(f"✅ 成功: {success_count} 个")
    print(f"❌ 失败: {fail_count} 个")
    
    if failed_fonts:
        print(f"\n失败的字体:")
        for font in failed_fonts:
            print(f"  - {font}")
    
    print("=" * 60)

if __name__ == "__main__":
    main()